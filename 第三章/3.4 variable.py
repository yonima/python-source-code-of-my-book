"""
    变量与常量
    1。 定义什么是变量
     变量名= 值

    2。 命名规则

    3. 命名方法
"""

my_name = '刘德华'

print(my_name)

# 再次赋值
my_name = '周杰伦'
print(my_name)

# 大小写敏感
My_name = '周杰伦'
print(My_name)

'''
    变量命名规则
'''
